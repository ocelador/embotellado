<template>
  <div v-if="visible" class="modal-overlay">
    <div class="modal-container">
      <div class="modal-header">
        <h3>{{ title }}</h3>
      </div>
      <div class="modal-body">
        <p>{{ message }}</p>
      </div>
      <div class="modal-footer">
        <button v-if="title === 'Confirmar Eliminación'" @click="confirm" class="btn btn-primary">Confirmar</button>
        <button @click="cancel" class="btn btn-secondary">Cancelar</button>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    visible: {
      type: Boolean,
      required: true
    },
    title: {
      type: String,
      default: 'Confirmación'
    },
    message: {
      type: String,
      default: '¿Estás seguro de que deseas eliminar este elemento?'
    }
  },
  methods: {
    confirm() {
      this.$emit('confirm');
